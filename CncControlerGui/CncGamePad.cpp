#include "CncCommon.h"
#include "CncGamePad.h"

/////////////////////////////////////////////////////////////
bool CncGamepad::isAnyStickActive() const {
/////////////////////////////////////////////////////////////
	return     isLeftStickActive() 
			|| isRightStickActive()
			;
}
/////////////////////////////////////////////////////////////
bool CncGamepad::isLeftStickActive() const {
/////////////////////////////////////////////////////////////
	return     !cnc::dblCompareNull(getLeftStickX())
			|| !cnc::dblCompareNull(getLeftStickY())
			;
}
/////////////////////////////////////////////////////////////
bool CncGamepad::isRightStickActive() const {
/////////////////////////////////////////////////////////////
	return     !cnc::dblCompareNull(getRightStickX())
			|| !cnc::dblCompareNull(getRightStickX())
			;
}
/////////////////////////////////////////////////////////////
bool CncGamepad::isNaviButtonActive() const {
/////////////////////////////////////////////////////////////
	return     stateButtonLeft()
			|| stateButtonRight()
			|| stateButtonUp()
			|| stateButtonDown()
			;
}
/////////////////////////////////////////////////////////////
bool CncGamepad::hasEmptyMovement() const {
/////////////////////////////////////////////////////////////
	return     isAnyStickActive()   == false
			&& isNaviButtonActive() == false
			;
}

