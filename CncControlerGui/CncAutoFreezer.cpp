#include "CncAutoFreezer.h"

//////////////////////////////////////////////////////////////////
CncAutoFreezer::CncAutoFreezer(wxWindow* w)
: ctrl(w) 
//////////////////////////////////////////////////////////////////
{
	if ( ctrl != NULL && ctrl->IsFrozen() == false )
		ctrl->Freeze();
}
//////////////////////////////////////////////////////////////////
CncAutoFreezer::~CncAutoFreezer() {
//////////////////////////////////////////////////////////////////
	traw();
}
//////////////////////////////////////////////////////////////////
void CncAutoFreezer::traw() {
//////////////////////////////////////////////////////////////////
	if ( ctrl != NULL && ctrl->IsFrozen() == true )
		ctrl->Thaw();
}
