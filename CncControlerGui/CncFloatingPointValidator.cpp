#include <iostream>
#include "CncFloatingPointValidator.h"

////////////////////////////////////////////////////////////////////
CncFloatingPointValidator::CncFloatingPointValidator(int precision, float *value, int style)
: wxFloatingPointValidator( precision, value, style)
, minValue(0.0)
, maxValue(0.0)
////////////////////////////////////////////////////////////////////
{}
////////////////////////////////////////////////////////////////////
CncFloatingPointValidator::~CncFloatingPointValidator() {
////////////////////////////////////////////////////////////////////
}
////////////////////////////////////////////////////////////////////
void CncFloatingPointValidator::SetMin(ValueType min) {
////////////////////////////////////////////////////////////////////
	wxFloatingPointValidator<float>::SetMin(min);
	minValue = min;
}
////////////////////////////////////////////////////////////////////
void CncFloatingPointValidator::SetMax(ValueType max) {
////////////////////////////////////////////////////////////////////
	wxFloatingPointValidator<float>::SetMax(max);
	maxValue = max;
}
////////////////////////////////////////////////////////////////////
void CncFloatingPointValidator::SetRange(ValueType min, ValueType max) {
////////////////////////////////////////////////////////////////////
	wxFloatingPointValidator<float>::SetRange(min, max);
	minValue = min;
	maxValue = max;
}
